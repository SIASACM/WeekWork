# include<stdio.h>
/**
题目要求： 
报数序列是一个整数序列，
按照其中的整数的顺序进行报数，
得到下一个数。其前五项如下：
1.     1
2.     11
3.     21
4.     1211
5.     111221
1 被读作  "one 1"  ("一个一") , 即 11。
11 被读作 "two 1s" ("两个一"）, 即 21。
21 被读作 "one 2",  "one 1" （"一个二" ,  "一个一") , 即 1211。

给定一个正整数 n（1 ≤ n ≤ 30），输出报数序列的第 n 项。

注意：整数顺序将表示为一个字符串。
 
// 题目十分难以理解 
/** 解法：递归  
1. 如果碰到连续几个相同的数字 --- return 个数 + 数字
2. 如果碰到单独的一个数字 ------- return  1 + 数字 
**/ 
har* countAndSay(int n) 
{
    char * str = (char *)malloc(sizeof(char) * 5000);
    char temp[5000];
    str[0] = '1';// 初始化默认值 
    int k = 0;
    for(int i = 1;i < n;i++)
    {
        for(int j = 0,count = 1;j < strlen(str);j++, count = 1)
        {
            while(str[j] == str[j+1])		//统计字符出现次数
            {
                count++; // 字符出现次数 
                j++;
            }
            temp[k++] = count + '0';	//将字符次数存入一个临时字符数组
            temp[k++] = str[j];			// 将字符存入 
        }
        temp[k] = '\0';					//最后加上结尾符
        strcpy(str, temp); // 赋给 
        k = 0;	//重置临时字符数组下标
    }
    return str;
}
